func TestCase1(t *testing.T) {
	sl := []int{8, 5, 7, 2, 3, 1, 1, 1}
	ans := []int{1, 1, 1, 2, 3, 5, 7, 8}
	ansF := mergeSort(sl)

	if !reflect.DeepEqual(ans, ansF) {
		t.Fatalf("This answer not equal :\n%v\n%v", ans, ansF)
	}

	t.Log("True Answer! :", ansF)
}

